public final class LearningModelStatusException extends Exception {
  public LearningModelStatusException() {
    super();
  }

  public LearningModelStatusException(String message) {
    super(message);
  }

  public LearningModelStatusException(String message, Throwable cause) {
    super(message, cause);
  }

  public LearningModelStatusException(Throwable cause) {
    super(cause);
  }
}
